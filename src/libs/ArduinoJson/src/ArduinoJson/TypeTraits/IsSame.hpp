#pragma once

namespace ArduinoJson {
namespace Internals {

// A meta-function that returns true if types T and U are the same.
template <typename T, typename U>
struct IsSame {
  static const bool value = false;
};

template <typename T>
struct IsSame<T, T> {
  static const bool value = true;
};
}
}
